public boolean validateSignup(){
        String login=jTextField2.getText();
            String pwd = String.valueOf(jPasswordField1.getPassword());
            String role=jTextField1.getText();
        
        if (login.equals("")){
            JOptionPane.showMessageDialog(this, "please enter user name ");
            return false;
            
        }
        if ( pwd.equals("") ){
            JOptionPane.showMessageDialog(this, "please enter user password ");
            return false;
            
            
        }
         if ( role.equals("") ){
            JOptionPane.showMessageDialog(this, "please enter user role=USER ");
            return false;
            
            
        }
       
        return true;
    }
